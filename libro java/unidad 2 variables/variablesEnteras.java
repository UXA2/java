public class variablesEnteras { 
    public static void main(String[] args) {
        int x; // se declara la varible como entera

        x = 5; //se asigna un valor 5 a la varible x
        System.out.println("el valor actual de mi varible" + x);

        x = 7; //se asigna el valor a 7 a la varible x 
        System.out.println(" y ahora es" + x);
    }
}